package com.py.app.telecom.vo;

/**通话参数虚体
 * Created by jzl on 17/12/20.
 */
public class CallsVo {
    private String startTime;//开始时间
    private String useTime; //用时多少秒
    private String place;       //位置
    private String callType;    //子类型
    private String otherCellPhone;  //对方号码
    private String cellPhone;       //本机号码

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getUseTime() {
        return useTime;
    }

    public void setUseTime(String useTime) {
        this.useTime = useTime;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getCallType() {
        return callType;
    }

    public void setCallType(String callType) {
        this.callType = callType;
    }

    public String getOtherCellPhone() {
        return otherCellPhone;
    }

    public void setOtherCellPhone(String otherCellPhone) {
        this.otherCellPhone = otherCellPhone;
    }

    public String getCellPhone() {
        return cellPhone;
    }

    public void setCellPhone(String cellPhone) {
        this.cellPhone = cellPhone;
    }
}
